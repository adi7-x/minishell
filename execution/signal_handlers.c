#include "../minishell.h"

void	sigint_handler(int sig)
{
	(void)sig;
	g_global.signal_received = 1;
	write(STDOUT_FILENO, "\n", 1);
	rl_on_new_line();
	rl_replace_line("", 0);
	rl_redisplay();
}

void	setup_signals(void)
{
	signal(SIGINT, sigint_handler);
	signal(SIGQUIT, SIG_IGN);
}

void	handle_heredoc_signal(int sig)
{
	(void)sig;
	exit(130);
}
